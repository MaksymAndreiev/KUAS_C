#include <stdio.h>

int main() {
    int tab = '\t';
    int newline = '\n';
    int space = ' ';

    printf("Numeric value of \\t: %d\n", tab);
    printf("Numeric value of \\n: %d\n", newline);
    printf("Numeric value of (space): %d\n", space);

    return 0;
}
